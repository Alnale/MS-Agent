/// Type-safe shared resource pool (replaces DashMap<String, Arc<dyn Any>>)
#[derive(Debug, Clone, Default)]
pub struct ResourcePool {
    resources: DashMap<String, Arc<dyn Any + Send + Sync>>,
}

impl ResourcePool {
    pub fn new() -> Self {
        Self {
            resources: DashMap::new(),
        }
    }

    /// Get a typed resource by name
    pub fn get<T: Send + Sync + 'static>(&self, name: &str) -> Option<Arc<T>> {
        self.resources
            .get(name)
            .and_then(|r| r.value().clone().downcast::<T>().ok())
    }

    /// Register a typed resource
    pub fn register<T: Send + Sync + 'static>(&self, name: &str, resource: Arc<T>) {
        self.resources.insert(name.to_string(), resource);
    }

    /// Remove a resource
    pub fn remove(&self, name: &str) -> Option<Arc<dyn Any + Send + Sync>> {
        self.resources.remove(name).map(|(_, v)| v)
    }

    /// Check if a resource exists
    pub fn contains(&self, name: &str) -> bool {
        self.resources.contains_key(name)
    }
}

/// Standardized Tool definition (compatible with OpenAI Functions / Anthropic Tools / MCP)
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Tool {
    pub name: String,
    pub description: String,
    pub parameters: ToolParameters,
    /// Tool type: "function" (default, locally executed) or platform tools like "web_search".
    /// Non-function tools are included in LLM requests but not executed locally.
    #[serde(default = "default_tool_type")]
    pub tool_type: String,
    /// Executor ID for routing to concrete implementation
    pub executor_id: String,
    /// Permission tags for policy control
    pub permission_tags: Vec<String>,
    /// Whether parallel invocation is allowed
    pub allow_parallel: bool,
    /// Default timeout in milliseconds
    pub default_timeout_ms: u64,
    /// Human-readable data flow hints for the LLM.
    /// Describes what kind of input this tool expects and what it outputs,
    /// helping the LLM plan multi-step tool chains.
    /// Example: "需要文件路径作为输入，可先用 file(write) 写入上下文数据"
    #[serde(default)]
    pub data_flow_hints: Vec<String>,
    /// Names of tools that should typically be called before this one.
    /// Used by the LLM to understand prerequisite steps.
    #[serde(default)]
    pub prerequisites: Vec<String>,
    /// Describes the output fields this tool produces on success.
    /// Helps the LLM know what data is available for chaining to the next tool.
    /// Format: "field_name: description"
    /// Example: ["path: 写入的文件路径", "bytes_written: 写入字节数"]
    #[serde(default)]
    pub output_fields: Vec<String>,
}

fn default_tool_type() -> String {
    "function".to_string()
}

