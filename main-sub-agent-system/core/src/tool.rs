/// Tool permission policy rule
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ToolPolicyRule {
    /// Tool name pattern (supports wildcards)
    pub tool_pattern: String,
    /// Required permission tags
    pub required_permissions: Vec<String>,
    /// Allowed agent IDs
    pub allowed_agents: Option<Vec<String>>,
    /// Allowed user IDs
    pub allowed_users: Option<Vec<String>>,
    /// Max calls per minute
    pub rate_limit: Option<u32>,
    /// Whether human approval is required (blocks execution until approved)
    pub requires_approval: bool,
}

/// Tool permission policy engine
pub struct ToolPolicyEngine {
    pub rules: Vec<ToolPolicyRule>,
    /// Rate limit tracking: "rule_index:tool_name" -> timestamps of recent calls
    rate_limit_state: Arc<DashMap<String, Vec<std::time::Instant>>>,
}

impl std::fmt::Debug for ToolPolicyEngine {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("ToolPolicyEngine")
            .field("rules", &self.rules)
            .finish()
    }
}

impl Clone for ToolPolicyEngine {
    fn clone(&self) -> Self {
        Self {
            rules: self.rules.clone(),
            rate_limit_state: self.rate_limit_state.clone(),
        }
    }
}

impl Default for ToolPolicyEngine {
    fn default() -> Self {
        Self {
            rules: Vec::new(),
            rate_limit_state: Arc::new(DashMap::new()),
        }
    }
}

impl ToolPolicyEngine {
    pub fn check_permission(&self, ctx: &ToolExecutionContext, call: &ToolCall) -> Result<()> {
        match self.check_permission_inner(ctx, call, false)? {
            PermissionDecision::Allowed => Ok(()),
            PermissionDecision::PendingApproval { message } => {
                Err(AgentTeamsError::ToolNotFound(message))
            }
        }
    }

    fn match_pattern(pattern: &str, name: &str) -> bool {
        if pattern == "*" {
            return true;
        }
        if let Some(prefix) = pattern.strip_suffix('*') {
            return name.starts_with(prefix);
        }
        pattern == name
    }

    /// Check if a tool requires human approval
    pub fn requires_approval(&self, tool_name: &str) -> bool {
        self.rules.iter().any(|rule| {
            Self::match_pattern(&rule.tool_pattern, tool_name) && rule.requires_approval
        })
    }

    /// Check permission with approval-aware semantics.
    /// Returns Ok(Pending) if approval is needed, Ok(Allowed) if permitted, Err if denied.
    pub fn check_permission_with_approval(
        &self,
        ctx: &ToolExecutionContext,
        call: &ToolCall,
        skip_approval: bool,
    ) -> Result<PermissionDecision> {
        self.check_permission_inner(ctx, call, skip_approval)
    }

    /// Shared permission check logic used by both check_permission and check_permission_with_approval.
    /// First matching rule wins — if no rule matches, the tool is allowed by default.
    fn check_permission_inner(
        &self,
        ctx: &ToolExecutionContext,
        call: &ToolCall,
        skip_approval: bool,
    ) -> Result<PermissionDecision> {
        for (i, rule) in self.rules.iter().enumerate() {
            if !Self::match_pattern(&rule.tool_pattern, &call.name) {
                continue;
            }

            // First matching rule wins — check agent permission
            if let Some(ref agents) = rule.allowed_agents {
                if !agents.contains(&ctx.agent_id) {
                    return Err(AgentTeamsError::ToolNotFound(format!(
                        "Permission denied: agent '{}' not allowed to call tool '{}'",
                        ctx.agent_id, call.name
                    )));
                }
            }

            // Check user permission
            if let Some(ref users) = rule.allowed_users {
                if let Some(ref uid) = ctx.user_id {
                    if !users.contains(uid) {
                        return Err(AgentTeamsError::ToolNotFound(format!(
                            "Permission denied: user '{}' not allowed to call tool '{}'",
                            uid, call.name
                        )));
                    }
                }
            }

            // Check rate limit
            if let Some(limit) = rule.rate_limit {
                let key = format!("{}:{}", i, call.name);
                let now = std::time::Instant::now();
                let one_minute = std::time::Duration::from_secs(60);

                let mut entry = self.rate_limit_state.entry(key).or_default();
                entry.retain(|t| now.saturating_duration_since(*t) < one_minute);

                if entry.len() as u32 >= limit {
                    return Err(AgentTeamsError::ToolNotFound(format!(
                        "Rate limit exceeded for tool '{}' ({} calls/min)",
                        call.name, limit
                    )));
                }
                entry.push(now);
            }

            // Check approval requirement
            if rule.requires_approval && !skip_approval {
                return Ok(PermissionDecision::PendingApproval {
                    message: format!(
                        "Tool '{}' requires human approval before execution",
                        call.name
                    ),
                });
            }

            // First matching rule processed — stop here
            break;
        }
        Ok(PermissionDecision::Allowed)
    }

    /// Clean up rate limit state for a removed tool.
    /// Call this when a tool is unregistered to prevent memory leaks.
    pub fn cleanup_tool(&self, tool_name: &str) {
        self.rate_limit_state.retain(|key, _| {
            // Keys are "rule_index:tool_name" — remove entries matching this tool
            !key.ends_with(&format!(":{}", tool_name))
        });
    }
}

/// Permission check result
#[derive(Debug, Clone)]
pub enum PermissionDecision {
    /// Tool call is allowed
    Allowed,
    /// Tool call requires human approval
    PendingApproval { message: String },
}

/// Unified Tool Registry — global tool registration center
pub struct UnifiedToolRegistry {
    tools: DashMap<String, Tool>,
    executors: DashMap<String, Arc<dyn ToolExecutor>>,
    policy_engine: ToolPolicyEngine,
    /// Tool category index for fast retrieval
    category_index: DashMap<String, Vec<String>>,
    /// Tool dependency graph (tool_name -> depends_on)
    dependency_graph: DashMap<String, Vec<String>>,
}

impl UnifiedToolRegistry {
    pub fn new() -> Self {
        Self {
            tools: DashMap::new(),
            executors: DashMap::new(),
            policy_engine: ToolPolicyEngine::default(),
            category_index: DashMap::new(),
            dependency_graph: DashMap::new(),
        }
    }

    /// Register an executor and auto-register its tools
    pub fn register_executor(&self, executor: Arc<dyn ToolExecutor>) {
        let id = executor.executor_id().to_string();
        tracing::info!("Registering tool executor: {}", id);
        self.executors.insert(id.clone(), executor.clone());

        for tool in executor.list_tools() {
            self.tools.insert(tool.name.clone(), tool);
        }
    }

    /// Get tool definition
    pub fn get_tool(&self, name: &str) -> Option<Tool> {
        self.tools.get(name).map(|t| t.clone())
    }

    /// List all available tools
    pub fn list_tools(&self) -> Vec<Tool> {
        self.tools.iter().map(|t| t.value().clone()).collect()
    }

    /// List tools by category
    pub fn list_by_category(&self, category: &str) -> Vec<Tool> {
        if let Some(names) = self.category_index.get(category) {
            names
                .iter()
                .filter_map(|name| self.get_tool(name))
                .collect()
        } else {
            Vec::new()
        }
    }

    /// Register a tool with optional category
    pub fn register_tool(&self, tool: Tool, category: Option<&str>) {
        if let Some(cat) = category {
            self.category_index
                .entry(cat.to_string())
                .or_default()
                .push(tool.name.clone());
        }
        self.tools.insert(tool.name.clone(), tool);
    }

    /// Unregister a tool and clean up associated state (rate limits, etc.)
    pub fn unregister(&self, tool_name: &str) -> Option<Tool> {
        // Remove from category index
        for mut entry in self.category_index.iter_mut() {
            entry.value_mut().retain(|n| n != tool_name);
        }
        // Remove from dependency graph
        self.dependency_graph.remove(tool_name);
        // Clean up rate limit state
        self.policy_engine.cleanup_tool(tool_name);
        self.tools.remove(tool_name).map(|(_, t)| t)
    }

    /// Set dependencies for a tool
    pub fn set_dependencies(&self, tool_name: &str, depends_on: Vec<String>) {
        self.dependency_graph
            .insert(tool_name.to_string(), depends_on);
    }

    /// Get dependencies for a tool
    pub fn get_dependencies(&self, tool_name: &str) -> Vec<String> {
        self.dependency_graph
            .get(tool_name)
            .map(|d| d.value().clone())
            .unwrap_or_default()
    }

    /// Execute a tool call (with permission check and timeout)
    pub async fn execute(&self, call: &ToolCall, ctx: &ToolExecutionContext) -> Result<ToolResult> {
        self.policy_engine.check_permission(ctx, call)?;
        self.execute_core(call, ctx, None).await
    }

    /// Execute a tool call with real-time status events.
    /// The `event_sink` receives ToolStatusEvent as the execution progresses.
    /// If `skip_approval` is true, bypasses human approval requirements.
    pub async fn execute_with_events(
        &self,
        call: &ToolCall,
        ctx: &ToolExecutionContext,
        skip_approval: bool,
        event_sink: Option<&tokio::sync::mpsc::UnboundedSender<ToolStatusEvent>>,
    ) -> Result<ToolResult> {
        // Permission check with approval awareness
        let decision = self
            .policy_engine
            .check_permission_with_approval(ctx, call, skip_approval)?;

        match decision {
            PermissionDecision::PendingApproval { message } => {
                if let Some(sink) = event_sink {
                    let _ = sink.send(ToolStatusEvent::ApprovalRequired {
                        call_id: call.id.clone(),
                        tool_name: call.name.clone(),
                        arguments: call.arguments.clone(),
                        approval_message: message.clone(),
                    });
                }
                return Err(AgentTeamsError::ToolPendingApproval {
                    tool_name: call.name.clone(),
                    message,
                });
            }
            PermissionDecision::Allowed => {}
        }

        if let Some(sink) = event_sink {
            let _ = sink.send(ToolStatusEvent::Preparing {
                call_id: call.id.clone(),
                tool_name: call.name.clone(),
                arguments: call.arguments.clone(),
            });
        }

        let result = self.execute_core(call, ctx, event_sink).await;

        match &result {
            Ok(tool_result) => {
                if let Some(sink) = event_sink {
                    let _ = sink.send(ToolStatusEvent::Completed {
                        call_id: tool_result.call_id.clone(),
                        tool_name: tool_result.name.clone(),
                        success: tool_result.success,
                        output: tool_result.output.clone(),
                        error: tool_result.error.clone(),
                        duration_ms: tool_result.execution_duration_ms,
                    });
                }
            }
            Err(e) => {
                if let Some(sink) = event_sink {
                    let _ = sink.send(ToolStatusEvent::Error {
                        call_id: call.id.clone(),
                        tool_name: call.name.clone(),
                        error: e.to_string(),
                    });
                }
            }
        }

        result
    }

    /// Core execution logic shared by execute() and execute_with_events().
    /// Resolves tool -> executor, emits executing event, runs with timeout.
    async fn execute_core(
        &self,
        call: &ToolCall,
        ctx: &ToolExecutionContext,
        event_sink: Option<&tokio::sync::mpsc::UnboundedSender<ToolStatusEvent>>,
    ) -> Result<ToolResult> {
        let tool = self.get_tool(&call.name).ok_or_else(|| {
            AgentTeamsError::ToolNotFound(call.name.clone())
        })?;

        tracing::debug!("Executing tool '{}', executor_id='{}'", call.name, tool.executor_id);

        let executor = self.executors.get(&tool.executor_id).ok_or_else(|| {
            AgentTeamsError::ToolNotFound(format!("Executor not found: {}", tool.executor_id))
        })?;

        if let Some(sink) = event_sink {
            let _ = sink.send(ToolStatusEvent::Executing {
                call_id: call.id.clone(),
                tool_name: call.name.clone(),
            });
        }

        let timeout = Duration::from_millis(tool.default_timeout_ms);
        let result = tokio::time::timeout(timeout, executor.execute(call, ctx)).await;

        match result {
            Ok(Ok(tool_result)) => Ok(tool_result),
            Ok(Err(e)) => Err(e),
            // Return Err rather than Ok(success=false) so the retry layer
            // (execute_with_retry_core) can retry transient slowdowns and
            // the circuit breaker records the failure. Previously a timeout
            // was silently swallowed — never retried, never tripped the CB.
            Err(_) => Err(AgentTeamsError::ToolTimeout {
                tool_name: call.name.clone(),
                timeout_ms: tool.default_timeout_ms,
            }),
        }
    }

    /// Get reference to policy engine
    pub fn policy_engine(&self) -> &ToolPolicyEngine {
        &self.policy_engine
    }

    /// Get mutable reference to policy engine
    pub fn policy_engine_mut(&mut self) -> &mut ToolPolicyEngine {
        &mut self.policy_engine
    }
}

