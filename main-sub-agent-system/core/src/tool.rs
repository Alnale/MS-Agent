/// Tool execution status — real-time progress events streamed to the frontend
#[derive(Debug, Clone, Serialize, Deserialize, utoipa::ToSchema)]
#[serde(tag = "status")]
pub enum ToolStatusEvent {
    /// Tool call is being prepared
    #[serde(rename = "preparing")]
    Preparing {
        call_id: String,
        tool_name: String,
        #[schema(value_type = Object)]
        arguments: Value,
    },
    /// Tool is executing
    #[serde(rename = "executing")]
    Executing {
        call_id: String,
        tool_name: String,
    },
    /// Tool completed successfully
    #[serde(rename = "completed")]
    Completed {
        call_id: String,
        tool_name: String,
        success: bool,
        #[schema(value_type = Object)]
        output: Value,
        error: Option<String>,
        duration_ms: u64,
    },
    /// Tool requires human approval before execution
    #[serde(rename = "approval_required")]
    ApprovalRequired {
        call_id: String,
        tool_name: String,
        #[schema(value_type = Object)]
        arguments: Value,
        approval_message: String,
    },
    /// Tool was approved and is now executing
    #[serde(rename = "approved")]
    Approved {
        call_id: String,
        tool_name: String,
    },
    /// Tool was rejected by the user
    #[serde(rename = "rejected")]
    Rejected {
        call_id: String,
        tool_name: String,
        reason: Option<String>,
    },
    /// Tool execution failed with error
    #[serde(rename = "error")]
    Error {
        call_id: String,
        tool_name: String,
        error: String,
    },
}

/// Tool execution context
#[derive(Debug, Clone)]
pub struct ToolExecutionContext {
    pub session_id: String,
    pub user_id: Option<String>,
    pub agent_id: String,
    pub request_id: String,
    /// Tool call history in the current turn
    pub tool_history: Vec<(ToolCall, ToolResult)>,
    /// Type-safe shared resource pool (replaces DashMap<String, Arc<dyn Any>>)
    pub resources: Arc<ResourcePool>,
    /// Agent context cache — injected directly from the agent pipeline.
    /// Tools can access working memory, system instructions, conversation history,
    /// and domain state without needing separate cache lookups.
    pub agent_context: Option<Arc<crate::context::AgentContext>>,
}

impl ToolExecutionContext {
    /// Create a minimal context without agent context (for standalone tool calls)
    pub fn minimal(session_id: impl Into<String>, agent_id: impl Into<String>) -> Self {
        Self {
            session_id: session_id.into(),
            user_id: None,
            agent_id: agent_id.into(),
            request_id: uuid::Uuid::new_v4().to_string(),
            tool_history: vec![],
            resources: Arc::new(ResourcePool::new()),
            agent_context: None,
        }
    }

    /// Create a context with agent context injected (for pipeline tool calls)
    pub fn with_agent_context(
        session_id: impl Into<String>,
        agent_id: impl Into<String>,
        agent_ctx: Arc<crate::context::AgentContext>,
    ) -> Self {
        let session = session_id.into();
        Self {
            session_id: session.clone(),
            user_id: agent_ctx.user_id.clone(),
            agent_id: agent_id.into(),
            request_id: uuid::Uuid::new_v4().to_string(),
            tool_history: vec![],
            resources: Arc::new(ResourcePool::new()),
            agent_context: Some(agent_ctx),
        }
    }

    /// Get working memory from agent context (if available)
    pub fn working_memory(&self) -> &[crate::memory::MemoryEntry] {
        self.agent_context
            .as_ref()
            .map(|ctx| ctx.working_memory.as_slice())
            .unwrap_or(&[])
    }

    /// Get system instructions from agent context (if available)
    pub fn system_instructions(&self) -> &[String] {
        self.agent_context
            .as_ref()
            .map(|ctx| ctx.system_instructions.as_slice())
            .unwrap_or(&[])
    }

    /// Get recent conversation history from agent context (if available)
    pub fn recent_history(&self) -> &[serde_json::Value] {
        self.agent_context
            .as_ref()
            .map(|ctx| ctx.recent_history.as_slice())
            .unwrap_or(&[])
    }

    /// Get domain state from agent context (if available)
    pub fn domain_state(&self) -> Option<&serde_json::Value> {
        self.agent_context
            .as_ref()
            .map(|ctx| ctx.domain_state.as_ref())
    }

    /// Get user entity info from agent context (if available)
    pub fn entity(&self) -> Option<&serde_json::Value> {
        self.agent_context
            .as_ref()
            .and_then(|ctx| ctx.entity.as_ref())
    }

    /// Build a memory-enriched context string for tool prompts.
    /// Combines working memory, system instructions, and recent history into
    /// a single injectable string that tools can embed in their prompts.
    pub fn build_context_injection(&self) -> String {
        let mut parts = Vec::new();

        // Working memory (user profile, facts, summaries)
        if let Some(ctx) = &self.agent_context {
            let memory_prompt = ctx.build_memory_prompt();
            if !memory_prompt.is_empty() {
                parts.push(memory_prompt);
            }
        }

        // System instructions
        let instructions = self.system_instructions();
        if !instructions.is_empty() {
            parts.push(format!(
                "## 系统指令\n{}",
                instructions.join("\n")
            ));
        }

        // Recent history (last 5 turns, compact)
        let history = self.recent_history();
        if !history.is_empty() {
            let recent: Vec<String> = history
                .iter()
                .rev()
                .take(5)
                .map(|v| {
                    let s = v.to_string();
                    if s.len() > 500 {
                        let mut end = 500;
                        while end > 0 && !s.is_char_boundary(end) {
                            end -= 1;
                        }
                        format!("{}...", &s[..end])
                    } else {
                        s
                    }
                })
                .collect();
            parts.push(format!(
                "## 最近对话\n{}",
                recent.join("\n")
            ));
        }

        parts.join("\n\n")
    }
}

