/// Builder for constructing Tool definitions ergonomically.
///
/// # Example
/// ```ignore
/// let tool = ToolBuilder::new("get_weather")
///     .description("Get current weather for a city")
///     .param_string("city", "City name", true)
///     .param_enum("unit", "Temperature unit", &["celsius", "fahrenheit"], false)
///     .executor("http")
///     .tag("network")
///     .timeout(10_000)
///     .build();
/// ```
pub struct ToolBuilder {
    name: String,
    description: String,
    properties: serde_json::Map<String, Value>,
    required: Vec<String>,
    tool_type: String,
    executor_id: String,
    permission_tags: Vec<String>,
    allow_parallel: bool,
    default_timeout_ms: u64,
    data_flow_hints: Vec<String>,
    prerequisites: Vec<String>,
    output_fields: Vec<String>,
}

impl ToolBuilder {
    pub fn new(name: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            description: String::new(),
            properties: serde_json::Map::new(),
            required: Vec::new(),
            tool_type: "function".to_string(),
            executor_id: String::new(),
            permission_tags: Vec::new(),
            allow_parallel: true,
            default_timeout_ms: 30_000,
            data_flow_hints: Vec::new(),
            prerequisites: Vec::new(),
            output_fields: Vec::new(),
        }
    }

    pub fn description(mut self, desc: impl Into<String>) -> Self {
        self.description = desc.into();
        self
    }

    pub fn executor(mut self, id: impl Into<String>) -> Self {
        self.executor_id = id.into();
        self
    }

    /// Set the tool type (e.g., "function", "web_search").
    /// Non-function tools are platform-level and not executed locally.
    pub fn tool_type(mut self, tt: impl Into<String>) -> Self {
        self.tool_type = tt.into();
        self
    }

    pub fn tag(mut self, tag: impl Into<String>) -> Self {
        self.permission_tags.push(tag.into());
        self
    }

    pub fn allow_parallel(mut self, allow: bool) -> Self {
        self.allow_parallel = allow;
        self
    }

    pub fn timeout(mut self, ms: u64) -> Self {
        self.default_timeout_ms = ms;
        self
    }

    /// Add a data flow hint describing what kind of input this tool expects
    /// and what it outputs. Helps the LLM plan multi-step tool chains.
    pub fn data_flow(mut self, hint: impl Into<String>) -> Self {
        self.data_flow_hints.push(hint.into());
        self
    }

    /// Declare a prerequisite tool that should typically be called before this one.
    pub fn requires(mut self, tool_name: impl Into<String>) -> Self {
        self.prerequisites.push(tool_name.into());
        self
    }

    /// Declare an output field this tool produces. Helps the LLM chain tools.
    /// Format: "field_name: description"
    pub fn output_field(mut self, field: impl Into<String>) -> Self {
        self.output_fields.push(field.into());
        self
    }

    /// Add a string parameter.
    pub fn param_string(self, name: impl Into<String>, desc: impl Into<String>, required: bool) -> Self {
        self.add_param(name, desc, "string", required, None, None)
    }

    /// Add an integer parameter.
    pub fn param_integer(self, name: impl Into<String>, desc: impl Into<String>, required: bool) -> Self {
        self.add_param(name, desc, "integer", required, None, None)
    }

    /// Add a number (float) parameter.
    pub fn param_number(self, name: impl Into<String>, desc: impl Into<String>, required: bool) -> Self {
        self.add_param(name, desc, "number", required, None, None)
    }

    /// Add a boolean parameter.
    pub fn param_bool(self, name: impl Into<String>, desc: impl Into<String>, required: bool) -> Self {
        self.add_param(name, desc, "boolean", required, None, None)
    }

    /// Add an object parameter.
    pub fn param_object(self, name: impl Into<String>, desc: impl Into<String>, required: bool) -> Self {
        self.add_param(name, desc, "object", required, None, None)
    }

    /// Add an array parameter.
    pub fn param_array(self, name: impl Into<String>, desc: impl Into<String>, required: bool) -> Self {
        self.add_param(name, desc, "array", required, None, None)
    }

    /// Add an enum parameter (string with allowed values).
    pub fn param_enum(
        self,
        name: impl Into<String>,
        desc: impl Into<String>,
        values: &[&str],
        required: bool,
    ) -> Self {
        self.add_param(
            name,
            desc,
            "string",
            required,
            Some(serde_json::json!(values)),
            None,
        )
    }

    /// Add a parameter with a default value.
    pub fn param_with_default(
        self,
        name: impl Into<String>,
        desc: impl Into<String>,
        json_type: impl Into<String>,
        default: Value,
    ) -> Self {
        self.add_param(name, desc, json_type, false, None, Some(default))
    }

    /// Add a raw JSON Schema property.
    pub fn param_raw(
        mut self,
        name: impl Into<String>,
        schema: Value,
        required: bool,
    ) -> Self {
        let n = name.into();
        if required {
            self.required.push(n.clone());
        }
        self.properties.insert(n, schema);
        self
    }

    fn add_param(
        mut self,
        name: impl Into<String>,
        desc: impl Into<String>,
        json_type: impl Into<String>,
        required: bool,
        enum_values: Option<Value>,
        default: Option<Value>,
    ) -> Self {
        let n = name.into();
        let mut prop = serde_json::json!({
            "type": json_type.into(),
            "description": desc.into(),
        });
        if let Some(vals) = enum_values {
            prop["enum"] = vals;
        }
        if let Some(def) = default {
            prop["default"] = def;
        }
        if required {
            self.required.push(n.clone());
        }
        self.properties.insert(n, prop);
        self
    }

    pub fn build(self) -> Tool {
        let mut schema = serde_json::json!({
            "type": "object",
            "properties": self.properties,
        });
        if !self.required.is_empty() {
            schema["required"] = serde_json::json!(self.required);
        }

        Tool {
            name: self.name,
            description: self.description,
            parameters: ToolParameters {
                schema,
                required: self.required,
            },
            tool_type: self.tool_type,
            executor_id: self.executor_id,
            permission_tags: self.permission_tags,
            allow_parallel: self.allow_parallel,
            default_timeout_ms: self.default_timeout_ms,
            data_flow_hints: self.data_flow_hints,
            prerequisites: self.prerequisites,
            output_fields: self.output_fields,
        }
    }
}

/// Declarative tool definition macro.
///
/// Generates a `Tool` struct with JSON Schema parameters from a concise DSL.
///
/// # Example
/// ```ignore
/// let weather_tool = tool!(
///     name: "get_weather",
///     description: "Get current weather for a city",
///     executor: "http",
///     tags: ["network"],
///     timeout: 10_000,
///     params: {
///         "city": (string, required, "City name"),
///         "unit": (enum ["celsius", "fahrenheit"], optional, "Temperature unit"),
///     }
/// );
/// ```
#[macro_export]
macro_rules! tool {
    (
        name: $name:expr,
        description: $desc:expr,
        executor: $exec:expr,
        tags: [$($tag:expr),* $(,)?],
        timeout: $timeout:expr,
        $(data_flow: [$($df:expr),* $(,)?],)?
        $(requires: [$($req:expr),* $(,)?],)?
        $(outputs: [$($out:expr),* $(,)?],)?
        params: {
            $( $pname:expr => ($($pdef:tt)+) ),* $(,)?
        }
    ) => {{
        let mut builder = $crate::tool::ToolBuilder::new($name)
            .description($desc)
            .executor($exec)
            .timeout($timeout);
        $( builder = builder.tag($tag); )*
        $( $( builder = builder.data_flow($df); )* )?
        $( $( builder = builder.requires($req); )* )?
        $( $( builder = builder.output_field($out); )* )*
        $( builder = $crate::tool!(@param builder, $pname, $($pdef)+); )*
        builder.build()
    }};
    // shorter form without tags/timeout
    (
        name: $name:expr,
        description: $desc:expr,
        executor: $exec:expr,
        $(data_flow: [$($df:expr),* $(,)?],)?
        $(requires: [$($req:expr),* $(,)?],)?
        $(outputs: [$($out:expr),* $(,)?],)?
        params: {
            $( $pname:expr => ($($pdef:tt)+) ),* $(,)?
        }
    ) => {{
        let mut builder = $crate::tool::ToolBuilder::new($name)
            .description($desc)
            .executor($exec);
        $( $( builder = builder.data_flow($df); )* )?
        $( $( builder = builder.requires($req); )* )?
        $( $( builder = builder.output_field($out); )* )*
        $( builder = $crate::tool!(@param builder, $pname, $($pdef)+); )*
        builder.build()
    }};

    (@param $builder:expr, $name:expr, string, required, $desc:expr) => {
        $builder.param_string($name, $desc, true)
    };
    (@param $builder:expr, $name:expr, string, optional, $desc:expr) => {
        $builder.param_string($name, $desc, false)
    };
    (@param $builder:expr, $name:expr, integer, required, $desc:expr) => {
        $builder.param_integer($name, $desc, true)
    };
    (@param $builder:expr, $name:expr, integer, optional, $desc:expr) => {
        $builder.param_integer($name, $desc, false)
    };
    (@param $builder:expr, $name:expr, number, required, $desc:expr) => {
        $builder.param_number($name, $desc, true)
    };
    (@param $builder:expr, $name:expr, number, optional, $desc:expr) => {
        $builder.param_number($name, $desc, false)
    };
    (@param $builder:expr, $name:expr, bool, required, $desc:expr) => {
        $builder.param_bool($name, $desc, true)
    };
    (@param $builder:expr, $name:expr, bool, optional, $desc:expr) => {
        $builder.param_bool($name, $desc, false)
    };
    (@param $builder:expr, $name:expr, object, required, $desc:expr) => {
        $builder.param_object($name, $desc, true)
    };
    (@param $builder:expr, $name:expr, object, optional, $desc:expr) => {
        $builder.param_object($name, $desc, false)
    };
    (@param $builder:expr, $name:expr, array, required, $desc:expr) => {
        $builder.param_array($name, $desc, true)
    };
    (@param $builder:expr, $name:expr, array, optional, $desc:expr) => {
        $builder.param_array($name, $desc, false)
    };
    (@param $builder:expr, $name:expr, enum [$($val:expr),+], required, $desc:expr) => {
        $builder.param_enum($name, $desc, &[$($val),+], true)
    };
    (@param $builder:expr, $name:expr, enum [$($val:expr),+], optional, $desc:expr) => {
        $builder.param_enum($name, $desc, &[$($val),+], false)
    };
}

