/// Tool call result
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ToolResult {
    pub call_id: String,
    pub name: String,
    pub success: bool,
    pub output: Value,
    pub error: Option<String>,
    pub execution_duration_ms: u64,
}

impl ToolResult {
    /// Compact representation for LLM context — strips verbose fields, truncates large output.
    /// Saves tokens when tool results are passed back as message history.
    /// Includes data flow hints to help the LLM chain tools effectively.
    pub fn compact(&self) -> Value {
        if self.success {
            // Directly clone the Value — avoid to_string() + from_str() roundtrip
            let output = if self.output.is_null() {
                Value::Null
            } else {
                let s = self.output.to_string();
                if s.len() > 16000 {
                    let mut end = 16000;
                    while end > 0 && !s.is_char_boundary(end) {
                        end -= 1;
                    }
                    Value::String(format!("{}...(truncated, {} chars total)", &s[..end], s.len()))
                } else {
                    self.output.clone()
                }
            };

            // Add data flow hints based on tool name
            let hint = match self.name.as_str() {
                "file" => {
                    if let Some(out) = self.output.as_object() {
                        if out.get("path").is_some() {
                            Some("文件已写入，path 可作为其它工具的输入参数")
                        } else if out.get("content").is_some() {
                            Some("文件内容已读取，可用于构造其它工具的参数")
                        } else {
                            None
                        }
                    } else {
                        None
                    }
                }
                _ => None,
            };

            // List output keys available for chaining to the next tool
            let chaining_keys: Vec<String> = match &self.output {
                Value::Object(map) => map.keys().cloned().collect(),
                _ => vec![],
            };

            let mut result = serde_json::json!({
                "tool": self.name,
                "ok": true,
                "output": output,
                "ms": self.execution_duration_ms,
            });
            if let Some(h) = hint {
                result["hint"] = Value::String(h.to_string());
            }
            if !chaining_keys.is_empty() {
                result["chaining_keys"] = Value::Array(
                    chaining_keys.into_iter().map(Value::String).collect()
                );
            }
            result
        } else {
            serde_json::json!({
                "tool": self.name,
                "ok": false,
                "error": self.error.as_deref().unwrap_or("unknown"),
                "ms": self.execution_duration_ms,
            })
        }
    }
}

/// Build a structured error ToolResult that gives the Agent enough information to self-correct.
///
/// Fields in the output:
/// - `success`: false
/// - `error`: human-readable error message
/// - `details`: technical details (OS error kind, path, etc.)
/// - `suggestion`: what the Agent should try next
/// - `tool`: tool name for context
pub fn tool_error(
    call: &ToolCall,
    error: impl Into<String>,
    details: impl Into<String>,
    suggestion: impl Into<String>,
    duration_ms: u64,
) -> ToolResult {
    let error = error.into();
    let details = details.into();
    let suggestion = suggestion.into();

    tracing::error!(
        tool = %call.name,
        call_id = %call.id,
        error = %error,
        details = %details,
        "Tool execution failed"
    );

    ToolResult {
        call_id: call.id.clone(),
        name: call.name.clone(),
        success: false,
        output: serde_json::json!({
            "success": false,
            "error": error,
            "details": details,
            "suggestion": suggestion,
            "tool": call.name,
        }),
        error: Some(error),
        execution_duration_ms: duration_ms,
    }
}

/// Build a success ToolResult with tracing.
pub fn tool_success(
    call: &ToolCall,
    output: serde_json::Value,
    duration_ms: u64,
) -> ToolResult {
    tracing::debug!(
        tool = %call.name,
        call_id = %call.id,
        duration_ms = duration_ms,
        "Tool execution succeeded"
    );

    ToolResult {
        call_id: call.id.clone(),
        name: call.name.clone(),
        success: true,
        output,
        error: None,
        execution_duration_ms: duration_ms,
    }
}

